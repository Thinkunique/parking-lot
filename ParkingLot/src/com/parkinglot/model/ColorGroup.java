package com.parkinglot.model;

import java.util.List;

public class ColorGroup {

	List<String> regNum;
	
	List<String> slotNum;

	/**
	 * @return the regNum
	 */
	public List<String> getRegNum() {
		return regNum;
	}

	/**
	 * @param regNum the regNum to set
	 */
	public void setRegNum(List<String> regNum) {
		this.regNum = regNum;
	}

	/**
	 * @return the slotNum
	 */
	public List<String> getSlotNum() {
		return slotNum;
	}

	/**
	 * @param slotNum the slotNum to set
	 */
	public void setSlotNum(List<String> slotNum) {
		this.slotNum = slotNum;
	}
	
	
	
}
